# EXIBINDO TIPOS DE VARIAVEIS
# COLOCAR O TYPE() PARA EXIBIR O TIPO DE VARIAVEL

# tipo int = inteiro
idade = 23
print(type(idade))

# tipo string = caractere
nome = "Mirian"
print(type(nome))

# tipo float = ponto flutuante ou decimal (casas decimais)
dinheiro = 7.67
print(type(dinheiro))

# tipo bool = verdadeiro ou falso (true e false)
ok = True
print(type(ok))

